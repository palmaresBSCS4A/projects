-- 1. Create the database
create database employee_attendance_system;
use employee_attendance_system;

create table employees (
    employee_id varchar(10) primary key,
    full_name varchar(100) not null,
    username varchar(50) not null unique,
    password varchar(255) not null,
    department varchar(50) not null,
    position varchar(50) not null,
    role enum('Admin', 'Employee') not null,
    contact_info varchar(100),
    date_hired date not null,
    status enum('Active', 'Inactive') not null
);

-- 3. Attendance table
create table attendance (
    attendance_id   int auto_increment primary key,
    employee_id     varchar(10) not null,
    date            date not null,
    time_in_am      time null,
    time_in_pm      time null,
    time_out_am     time null,
    time_out_pm     time null,
    total_hours     decimal(5,2) not null default 0.00,
    foreign key (employee_id) references employees(employee_id),
    unique key (employee_id, date)
);


-- 4. Leave Requests table with request_date
create table leave_requests (
    leave_id int auto_increment primary key,
    employee_id varchar(10) not null,
    leave_type enum('Vacation Leave', 'Sick Leave', 'Emergency Leave') not null,
    start_date date not null,
    end_date date not null,
    reason text,
    status enum('Pending', 'Approved', 'Rejected') default 'Pending',
    request_date timestamp default current_timestamp,
    foreign key (employee_id) references employees(employee_id)
);


