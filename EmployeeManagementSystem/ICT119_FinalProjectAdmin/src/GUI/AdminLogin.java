    private void adminLoginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminLoginBTNActionPerformed
        // TODO add your handling code here:
        String adminId = adminIDTF.getText().trim();
        String adminPass = new String(adminPassPF.getPassword()).trim();

        if (adminId.isEmpty() || adminPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = DBConnectionAdmin.getConnection()) {
            String sql = "SELECT * FROM employees WHERE employee_id = ? AND department = 'HR' AND position = 'Officer' AND role = 'Admin'";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, adminId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String dbPassword = rs.getString("password");
                String status = rs.getString("status");

                if (!"Active".equalsIgnoreCase(status)) {
                    JOptionPane.showMessageDialog(this, "Your account is inactive. Contact system administrator.", "Access Denied", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                if (adminPass.equals(dbPassword)) {
                    this.employeeId = rs.getString("employee_id");
                            
                    logAdminAction("Admin login successfully");
                    JOptionPane.showMessageDialog(this, "Login successful!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    String employeeId = rs.getString("employee_id");

                    AdminDashboard dashboard = new AdminDashboard(employeeId);
                    dashboard.setVisible(true);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(this, "Incorrect password.", "Login Failed", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Admin ID not found or does not meet admin criteria (MJAD###, HR, Officer).", "Login Failed", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error occurred.", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_adminLoginBTNActionPerformed

