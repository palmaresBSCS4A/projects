    private void userLoginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userLoginBTNActionPerformed
        // TODO add your handling code here:
        String empIdStr = empIdTF.getText().trim();
        String password = new String(empPassPF.getPassword()).trim();

        if (empIdStr.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter both Employee ID and Password.");
            return;
        }

        userLoginBTN.setEnabled(false);  

        if (!connectToServer()) {
            userLoginBTN.setEnabled(true);
            return;
        }

        try {
            String initialResponse = in.readLine();
            if (initialResponse == null || !initialResponse.equals("CONNECTED")) {
                JOptionPane.showMessageDialog(null, "Unexpected initial response: " + initialResponse);
                closeConnections();
                userLoginBTN.setEnabled(true);
                return;
            }

            out.println("LOGIN|" + empIdStr + "|" + password);

            String loginResponse = in.readLine();

            if (loginResponse == null) {
                JOptionPane.showMessageDialog(null, "No response from server. Connection lost.");
                closeConnections();
                userLoginBTN.setEnabled(true);
                return;
            }

            if ("LOGIN_SUCCESS".equals(loginResponse)) {
                JOptionPane.showMessageDialog(null, "Login successful!");
                // Success - keep connection open and proceed to dashboard
                Dashboard dashboard = new Dashboard(empIdStr, socket, out, in);
                dashboard.setVisible(true);
                this.dispose();
            } 
            else if (loginResponse.startsWith("LOGIN_FAILED")) {
                JOptionPane.showMessageDialog(null, "Login failed: " + 
                    (loginResponse.contains("|") ? loginResponse.split("\\|")[1] : "Invalid credentials"));
                closeConnections();
                userLoginBTN.setEnabled(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "Unexpected login response: " + loginResponse);
                closeConnections();
                userLoginBTN.setEnabled(true);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Connection error: " + e.getMessage());
            closeConnections();
            userLoginBTN.setEnabled(true);
        }
    }//GEN-LAST:event_userLoginBTNActionPerformed

